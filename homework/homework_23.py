"""Contactlist"""

# On the Login page:
# Enter email in field: #email
# Enter password in field: #password
# Click on the button: #submit

# 1. Create a new contact:
# Page https://thinking-tester-contact-list.herokuapp.com/contactList
# should be opened
# Click on the button: #add-contact
# Enter First name (required): #firstName
# Enter Last name (required): #lastName
# Enter Date of Birth (optional): #birthdate
# Enter phone (optional): #phone
# Enter street address 1 (optional): #street1
# Enter street address 2 (optional): #street2
# Enter city (optional): #city
# Enter state or province (optional): #stateProvince
# Enter postal code (optional): #postalCode
# Enter country (optional): #country
# Click on the button: #submit

# 2. Edit the contact:
# Page https://thinking-tester-contact-list.herokuapp.com/contactList
# should be opened
# Click on the row table: //tr[@class="contactTableBodyRow"][1]
# Click on the button: #edit-contact
# Change First name: #firstName
# Change Last name: #lastName
# Change Date of Birth: #birthdate
# Change phone: #phone
# Change street address 1: #street1
# Change street address 2: #street2
# Change city: #city
# Change state or province: #stateProvince
# Change postal code: #postalCode
# Change country: #country
# Click on the button: #submit

# 3. Delete the contact:
# Page https://thinking-tester-contact-list.herokuapp.com/contactList
# should be opened
# Click on the row table: //tr[@class="contactTableBodyRow"][1]
# Click on the button: #delete
# Press "Enter" on the keyboard
